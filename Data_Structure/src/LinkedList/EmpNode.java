package LinkedList;

public class EmpNode {
	int eid;
	String name,gender;
	Float salary;
	EmpNode next;
	
	public EmpNode(int eid, String name, String gender, Float salary) {
		this.eid = eid;
		this.name = name;
		this.gender = gender;
		this.salary = salary;
		next = null;
	}
	
	

}
